package TankWar;

/**
 * 方向枚举类
 * @author qbg
 * Direction
 */
public enum Direction {
	/**
	 * 水平向左
	 */
	L, 
	/**
	 * 左上方
	 */
	LU, 
	/**
	 * 竖直向上
	 */
	U, 
	/**
	 * 右上方
	 */
	RU, 
	/**
	 * 水平向右
	 */
	R, 
	/**
	 * 右下方
	 */
	RD, 
	/**
	 * 竖直向下
	 */
	D,
	/**
	 * 左下方
	 */
	LD,
	/**
	 * 原地不动
	 */
	STOP;
}
